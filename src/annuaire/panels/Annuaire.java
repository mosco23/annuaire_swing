    private void makCard(TreeSet<User> users){
        jPanel.removeAll();
        for(User user: users){
            if(user instanceof Student)
                jPanel.add(new CardStudent((Student) user));
            else
                jPanel.add(new CardAdmin((Admin) user));
        }
        
    }

